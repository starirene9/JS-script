//횟수 지정 반복문 3바퀴 루프


var i=1; //1. begin : 제어변수 선언
while(i <= 5) { //2. end : 조건식
    console.log('hello');  //3. 반복 실행 
    i++; //4. 증감식 
}
//======================================

for(var i=1;i <=5; i++) { //4 i++
    console.log('hello'); //3
}

for(var i=0;i <5; i++) { //4 i++
    console.log('hello'); //3
}
// for은 세 개가 모여있어서 몇 바퀴 돌지 바로 보임 